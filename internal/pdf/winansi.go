// unicodeToWinAnsi inverts winAnsiToUnicode. Unrepresentable codepoints
// fall back to '?' (0x3F) at write time — see encodeWinAnsi.
var unicodeToWinAnsi = func() map[uint32]byte {
	m := make(map[uint32]byte, 256)
	for b, u := range winAnsiToUnicode {
		if u != 0 && m[u] == 0 {
			m[u] = byte(b)
		}
	}
	return m
}()

// encodeWinAnsi returns the WinAnsiEncoding byte sequence for the supplied
// runes. Runes that lie outside WinAnsi's coverage are replaced by '?'
// — v0.1 limitation; the eventual Identity-H / Type 0 path will lift it.
func encodeWinAnsi(runes []rune) []byte {
	out := make([]byte, 0, len(runes))
	for _, r := range runes {
		if r < 128 {
			out = append(out, byte(r))
			continue
		}
		if b, ok := unicodeToWinAnsi[uint32(r)]; ok {
			out = append(out, b)
			continue
		}
		out = append(out, '?')
	}
	return out
}
